package datatype;

public class DataType {

    public static void main(String[] args) {
        //byte,short,int,long-for numerical value

        int a = 10;
        int b = 20;
        int result = a + b;
        System.out.println(result);//30

        //double and float- for decimal value
        double d = 5.54;
        float e = 10.5f;
        System.out.println(d);
        //character-for single character
        char c='K';
        System.out.println(c);//K

        //boolean-true or false
        boolean myResult=90>80;
        boolean myResult1=false;
        System.out.println(myResult1);


        //String - for sequence of characters
        String name= "Prime Testing";
        String myName= "Shylu Birundha";
        System.out.println(myName);
    }

}
